1.表达式,推荐为 整型或字串,不推荐bool 值
	因为 case 做的是 == 判断,不是严格判断
	true -> 1,false -> 0

2. switch 中遇到 break,才会跳出switch 结构
换言之,遇到了break才跳出方括号内的代码区间
